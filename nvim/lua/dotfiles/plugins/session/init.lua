local M = {}

local modules = {
  require("dotfiles.plugins.session.auto_session"),
  require("dotfiles.plugins.session.project"),
}

function M.setup(ctx)
  local specs = {}
  for _, mod in ipairs(modules) do
    local entries = mod(ctx)
    if entries ~= nil then
      for _, spec in ipairs(entries) do
        table.insert(specs, spec)
      end
    end
  end
  return specs
end

return M
